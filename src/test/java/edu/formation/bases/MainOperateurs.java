package edu.formation.bases;

public class MainOperateurs {

  public static void main(String[] args) {
    int i = 0;
    i += 1;
    // i == 1
    i -= 12;
    // i == -11
    i *= 2;
    // i == -22
    i++;
    // i == -21
    i--;
    // i == -22
    ++i;
    // i == -21
    System.out.println(i);
  }

}
